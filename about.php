					<h1>Missionette Statement</h1>
						<hr>
						<p>Architects are expected to have a handle on civil, material, and mechanical engineering, landscape architecture, building contracting and city codes, aesthetics, interior design, manipulating light, home energy consumption, and how people engage with buildings and each other. Interface designers require a similarly broad spectrum of skills from typography to layout and graphic design to interaction design to storytelling.</p>
						
						<p>The Design Axioms describe the minimal rule set for designing interfaces: the 16 foundational concepts that are required knowledge for engineers and designers to create usable and elegant interfaces. The design of the content (and book) will follow the same tenets outlined within it. Diagrams, screenshots, and pictures (the visual story) dominate the content; text-based descriptions are short and to the point... Why do we need this?</p>

						<p>We&#39;re all designers. We all make conscious and unconscious design decisions every day from the act of walking to lunch, creating a task list, or writing a technical review. Some of us are paid to design in disciplines that require planning the underlying purpose of products or planning the working of something before it is built. This is a re-education for designers. For 15 years, a production stratification has evolved into stove-piped disciplines from information architecture to user experience to interaction design to usability to graphic design much to the detriment of designers. Emerging from this new discipline of 30 years is the Renaissance interface designer: a technically savvy artist that&#39;s fluent in the fundamentals of typography and color theory.</p>
